public class datos {
    public static void main(String args[]){
        System.out.println(Byte.MAX_VALUE);
        System.out.println(Byte.MIN_VALUE);

        byte minValue = -128;
        byte maxValue = 127;
        System.out.println(minValue);
        System.out.println(maxValue);
        System.out.println("short máximo es: " + Short.MAX_VALUE);
        System.out.println("short mínimo es:" + Short.MIN_VALUE);

        short shortMinValue = 32767;
        short shortMaxValue = -32767;
        System.out.println(shortMinValue);
        System.out.print(shortMaxValue);

        System.out.println("entero máximo es:" + Integer.MAX_VALUE);
        System.out.println("entero mínimo es" + Integer.MIN_VALUE);

        System.out.println(Long.MAX_VALUE);
        System.out.println(Long.MAX_VALUE);

        System.out.println(Float.MAX_VALUE);
        System.out.println(Float.MIN_VALUE);

        System.out.println(Double.MAX_VALUE);
        System.out.println(Double.MIN_VALUE);

    }
}
